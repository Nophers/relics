Sakramente sind sichtbare Zeichen für unsichtbare Gnade, die durch Jesus Christmas und die Kirche vermittelt werden.
Sakramente sind äußere Zeichen, die mit deutenden Worten ein unsichtbares Geheimnis Gottes versinnbildlicht und darstellt.
Sakramente sind äußere Zeichen die Gottes Wirken in unserem Leben sichtbar machen wollen, ursprung Jesus Christus.

Sakramente:
- Taufe
- Firmung
- Eucharistie
- Busse
- Krankensalbung
- Ehe 
- Prieste

Die Anzahl varrierte von 2 (Taufe und Eucharistie) bis zu 30, die heuten eingeschlossen!
Sieben Zahl wurde auf den Konzil von Trient festgelegt JAHR 1543 - 1565.


Sakramentalion:

Sind heilige Handlungen, 
fürbittende Gebete der Kirche für bestimmte Aliegen (Weihwasserweihe, Haussegnung, Autosegnung, Adventkranzweihe, etc.)

Sinn der Sakramentalion:
Das ganze Leben soll unter dem besonderen Schutz Gottes stehen

Unterschiede Sakramentalion:
Weihungen: Mensch (z.B. Abt) oder ein Gegenstand (Kirchenglocken) werden für Gott oder für den Gottesdienst bestimmt!
Segnungen: Der Segen Gottes wird auf Menschen oder Dinge herabgerufen, ohne dass diese dem profanen Gebrauch entzogen werden (Auto)

